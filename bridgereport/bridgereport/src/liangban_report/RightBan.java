package liangban_report;

public class RightBan {
	private String idR;
	private String sortR;
	private String detailR;
	private String rankR;
	private double scoreR;
	private String psR;
	public String getIdR() {
		return idR;
	}
	public void setIdR(String idR) {
		this.idR = idR;
	}
	public String getSortR() {
		return sortR;
	}
	public void setSortR(String sortR) {
		this.sortR = sortR;
	}
	public String getDetailR() {
		return detailR;
	}
	public void setDetailR(String detailR) {
		this.detailR = detailR;
	}
	public String getRankR() {
		return rankR;
	}
	public void setRankR(String rankR) {
		this.rankR = rankR;
	}
	public double getScoreR() {
		return scoreR;
	}
	public void setScoreR(double scoreR) {
		this.scoreR = scoreR;
	}
	public String getPsR() {
		return psR;
	}
	public void setPsR(String psR) {
		this.psR = psR;
	}

}
